<h2>ROÑA (AGENTE CAUSAL: sphaceloma perseae jenkins) </h2>
<p>Se generan lesiones redondeadas o irregulares de color café claro, de apariencia corchosa, 
protuberante, que pueden cubrir gran parte del fruto, afectando de forma significativa el valor 
comercial del producto. Las lesiones de la roña son superficiales y no afectan la calidad de la pulpa. En 
las hojas jóvenes y brotes tiernos se observan diminutas lesiones (1 a 2 mm de diámetro) de color café 
oscuro, rodeadas de un leve halo clorótico (Bernal, 2014).</p>
<h3>Imagen</h3>
<h2>MANEJO</h2>
<p>Se deben realizar podas de aclareo, que permitan mayor luminosidad y aireación a los árboles. El manejo 
químico de la roña consiste en realizar aspersiones de fungicidas a base de Clorotalonil (Daconil 720 SC) 
(1 cc/l) (Control 500) (2,5 cc/l), Difenoconazol (Score 250 EC) (0,5 a 1 cc/l), Benomil (Benlate WP) (Bezil 
WP) (0,5 g/l), Oxicloruro de Cobre (Oxiclor 35 WP ) (2 g/l) o Hidróxido Cúprico (Kocide 101) (2 g/l), usados 
en rotación. Teniendo en cuenta que los trips favorecen el ataque del hongo causante de la roña, estos 
deben mantenerse en poblaciones bajas, mediante la aspersión de productos a base Clorfenapir 
(Sunfire 240 SC) (0,5 cc/l), Imidacloprid (Confidor SC 350) (Jade WG 70) (0,5 cc/l) o Cipermetrina 
(Cymbush EC) (0,5 cc/l). Las aspersiones de los fungicidas e insecticidas mencionados se deben realizar 
al inicio de la floración, hasta dos o tres semanas después del cuajamiento del fruto (Bernal, 2014).</p>
<h2>MANCHA ANGULAR DEL FRUTO (AGENTE CAUSAL: cercospora purpurea cooke)</h2>
<p>En los frutos los daños son de tamaño mediano (1 a 2 cm de diámetro), de color negro con bordes 
angulosos o irregulares, con bordes rojizos bien definidos. En las hojas se observan daños de tamaño 
pequeño (0,3 a 1 cm de diámetro), de color marrón o café oscuro, de formas irregulares o angulares. 
(Pablo J. Tamayo M., 2007).</p>
<h3>Imagen</h3>
<h2>MANEJO</h2>
<p>En condiciones de cultivo, se deben realizar podas de aclareo, que permitan mayor luminosidad y 
aireación a los árboles, teniendo en cuenta de sellar las heridas causadas por la poda, aplicando pintura 
a base de aceite, con brocha, en la región podada. Un adecuado manejo agronómico y una fertilización 
balanceada disminuyen la incidencia y severidad de la mancha angular. Se ha demostrado
experimentalmente, que el tratamiento de frutos de aguacate en precosecha, con aislamientos de 
Bacillus subtilis, reducen la severidad de la mancha negra del aguacate en almacenamiento. Para el 
manejo de la mancha angular, se recomiendan aspersiones foliares antes de la cosecha de productos a 
base de Oxicloruro de Cobre (Oxiclor 35 WP)(2 g/l), Hidróxido Cùprico (Kocide 101)(2 g/l), Benomil 
(Benlate WP)(Bezil WP)(0,5 g/l), Carbendazim (Derosal 500 SC)(0,75 a 1,25 cc/l)(Bavistin 500 SC)(0,5 
cc/l), Ciproconazol (Alto 100 SL)(0,25 a 0,5 cc/l) o Flusilazol (Punch EC)(0,15 a 0,25 cc/l), utilizados en 
rotación, los cuales son efectivos para prevenir la presencia de la enfermedad en los frutos en el campo 
y durante la poscosecha (Bernal, 2007).
</p>
