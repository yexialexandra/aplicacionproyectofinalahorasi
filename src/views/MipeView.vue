<h2>PUDRICIÓN DE LA RAÍZ (AGENTE CAUSAL : phytophthora cinnamomi rands)</h2>
<p>Phytophthora cinnamomi es la enfermedad de mayor importancia en el cultivo de aguacate en el mundo 
(Zentmyer et al., 1994), causa pudrición de raíces en todas las edades y se desarrolla de forma más 
rápida en suelos pesados con alto contenido de arcillas fácilmente encharcables. Afecta las raíces más
nuevas y finas, las cuales se tornan de color café-negro, la corteza de las raíces se desprende fácilmente 
aun cuando las raíces no presentan necrosis, estas raíces posteriormente mueren. Al examinar las raíces 
secundarias, presentan necrosis parcial. La muerte del árbol se puede tardar desde unos pocos meses 
hasta 2 o 3 años. En los estados iniciales de la enfermedad, las raicillas mueren y se pudren, por lo cual 
es difícil hallar raíces normales en árboles enfermos. Las raíces primarias y secundarias no presentan 
síntomas y son poco afectadas (ICA, 2012).</p>
<p>En estados avanzados, el daño se observa en las raíces principales, secundarias y el cuello, 
extendiéndose en el tronco del árbol hasta unos 50 cm aproximadamente. Se generan grandes áreas 
afectadas de tono marrón oscuro y necrótico, de poca profundidad en el leño. La falta de raíces nuevas 
y pelos absorbentes evita la toma de agua, en consecuencia, el suelo bajo las plantas enfermas tiende 
a mantenerse húmedo (ICA, 2012).</p>
