<h2>MANEJO</h2>
<p>Antes de pensar en las aplicaciones con productos químicos, se debe considerar un manejo preventivo, 
algunas prácticas recomendadas son:</p>
<ul>
  <li>Uso de materiales de siembra libres y tolerantes a la enfermedad.</li>
  <li>Evitar el flujo de agua de un lote infectado a lotes libres de la enfermedad. </li>
  <li>Establecer cultivos en lotes con buenas características de suelo.</li>
  <li>Fertilización balanceada y oportuna.</li>
  <li>Disponer puntos de desinfección de calzado y vehículos a la entrada los lotes.</li>
  <li>Adición de microorganismos antagonistas que protejan las raíces (Ej. Trichoderma sp)</li>
  <li>Desinfección regular de las herramientas y equipos.</li>
  <li>Erradicación de plantas enfermas. </li>
  <li>Evitar el establecimiento de cultivos de aguacate en lotes infectados con el patógeno.</li>
  <li>Evitar causar daños mecánicos al tallo y a las raíces. </li>
  <li>Manejo adecuado de arvenses</li>
</ul>
<p>Teniendo en cuenta las estrategias de control de humedad, aislamiento de fuentes de contaminación y 
demás; en plantaciones establecidas, se realiza aplicación, previa erradicación de malezas, de fungicidas 
a base Metalaxil+Mancozeb (Ridomil Gold MZ 68 WP) (3,75 g/l) o Fosetil Aluminio (Aliette 80 WP) (2,5 
a 3 g/l) (Fosetal 80 WP) (1,5 g/l), procurando cubrir y empapar toda el área de la raíz. Adicionalmente, 
se pueden realizar aspersiones foliares de fungicidas a base de Fosetil Aluminio (Aliette 80 WP) (2,5 a 3 
g/l) (Fosetal 80 WP)(1,5 g/l), fertilizantes ricos en fósforo y potasio (Codafol 0-3020)(3 cc/l) e inyecciones 
al tronco del patrón y la copa (Figura 14), de un producto a base de ácido fosforoso, ácido fosfónico, 
fosfitos o fosfonatos (Fosfito Mono/ Dibásico de Potasio) (Sephit K 30-20) (10 a 20 cc/l) (Manvert Fosika 
)(10 a 20 cc/l) (Nutriphite P+K) (10 cc/litro/m2 de área de raíz) (Agrifos 400 SL) (5 cc/5 cc de agua). 
(Ramírez, 2013).</p>
