<h2>MARCHITAMIENTO DE LA PLANTA DE AGUACATE (AGENTE CAUSAL: verticillium spp)</h2>
<p>Los síntomas de la enfermedad pueden confundirse con la de Phytophthora cinnamomi; este 
diagnóstico se debe realizar sobre un análisis de laboratorio. Los árboles afectados por Verticillium sp., 
detienen parcialmente su crecimiento. El hongo invade los tallos y ramas de un lado de la planta, 
produciendo marchitez repentina, parcial o total de hojas (Ploetz et al., 1994). Las hojas de las ramas
afectadas toman una coloración café y permanecen adheridas al árbol por algún tiempo y luego caen,
mientras que los frutos, se mantienen en el árbol. Posteriormente los frutos caen y se presenta un 
paloteo o muerte descendente de algunas ramas (Zentmyer, 1949; Ploetz et al., 1994).</p>
